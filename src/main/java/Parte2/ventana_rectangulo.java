        base = Integer.parseInt(txtEntradaBase.getText());
        altura = Integer.parseInt(txtEntradaAltura.getText());
        FG_Rectangulo rectangulo = new FG_Rectangulo(base,altura);
        txtSalidaArea.setText(String.valueOf(rectangulo.calcularArea()));
        txtSalidaPerimetro.setText(String.valueOf(rectangulo.calcularPerimetro()));
